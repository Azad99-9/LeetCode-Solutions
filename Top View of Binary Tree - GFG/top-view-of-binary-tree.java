class Solution
{
    //Function to return a list of nodes visible from the top view 
    //from left to right in Binary Tree.
    
    // hd -> horizontal distance from root
    
    static class Pair {
        Node node;
        int hd;
        Pair(Node _node, int _hd) {
            node = _node;
            hd = _hd;
        }
    }
    
    static ArrayList<Integer> topView(Node root)
    {
        Map<Integer,Integer> tracker = new TreeMap<>();
        Queue<Pair> bfsHelper = new LinkedList<>();
        if(root == null) return new ArrayList<>();
        bfsHelper.offer(new Pair(root, 0));
        while(!bfsHelper.isEmpty()) {
            
            Pair cur = bfsHelper.poll();
            int hd = cur.hd;
            Node temp = cur.node;
            
            if(!tracker.containsKey(hd))
                tracker.put(hd, temp.data);
                
            if(temp.left != null) bfsHelper.offer(new Pair(temp.left, hd - 1));
                
            if(temp.right != null) bfsHelper.offer(new Pair(temp.right, hd + 1));
            
        }
        return new ArrayList<>(tracker.values());
        
    }
}